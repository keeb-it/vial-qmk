static void render_layer(void) {
    oled_set_cursor(16,7);
    oled_write_raw_P(layer_icon, sizeof(layer_icon));
    oled_set_cursor(18,7);
    switch (get_highest_layer(layer_state)) {
        case _BASE:
            oled_write("QWR", false);
            break;
        case _NUM:
            oled_write("NUM", false);
            break;
        case _NAV:
            oled_write("NAV", false);
            break;
        default:
            oled_write("XXX", false);
    }
}

static void render_status(void)
{
    // Host Keyboard LED Status
    led_t led_state = host_keyboard_led_state();
    if (led_state.caps_lock) {
        oled_set_cursor(0, 7);
        oled_write_raw_P(capslock_icon, sizeof(capslock_icon));
    } else {
        oled_set_cursor(0, 7);
        oled_write("   ", false);
    }
    if (led_state.num_lock) {
        oled_set_cursor(3, 7);
        oled_write_raw_P(numlock_icon, sizeof(numlock_icon));
    } else {
        oled_set_cursor(3, 7);
        oled_write("   ", false);
    }
    if (led_state.scroll_lock) {
        oled_set_cursor(6, 7);
        oled_write_raw_P(scrolllock_icon, sizeof(scrolllock_icon));
    } else {
        oled_set_cursor(6, 7);
        oled_write("   ", false);
    }
}


